#include <iostream>
using namespace std;
int main()
{
	int ageOFHONG = 10, ageOFFATHER = 33, NUM = 0;
	for (NUM=1;2* ageOFHONG != ageOFFATHER;NUM)
	{
		ageOFHONG++;
		ageOFFATHER++;
		NUM++;
	}
	cout << ageOFHONG << endl;
	cout << ageOFFATHER << endl;
	cout << NUM << endl;
	return 0;
}
